package Day2;

public class LogicalOperatorExample {
    public static void main(String[] args) {

        int a = 20;
        int b = 10;
        int c = 50;

        System.out.println(a>b && b>c);
        System.out.println(a>b & b>c);
        System.out.println(a>b || b>c);
        System.out.println(a>b | b>c);


    }
}
