package Day2;

public class DataTypeExample {
    public static void main(String[] args) {

        byte var = 127; // -128 to 127

        short a1 = 6000; //-32,768 to 32,767

        int b1 = 54545; //-2,147,483,648 to 2,147,483,647

        long c1 = 1234567897;  //-9,223,372,036,854,775,808 to 9,223,372,036,854,775,807

        float d1 = 1.32345555557987957f;

        System.out.println(d1);

        double e1 = 1.32345555557987957;

        System.out.println(e1);

        boolean bool = true;

        System.out.println(bool);

        char c = 'A';

        String str = "shubham";

        System.out.println(str);
        System.out.println(c);

    }
}
